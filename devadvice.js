module.exports = {
    name: "نصيحة_اليوم",
    description: "يعطيك نصيحة ذهبية لتطوير مستقبلك وعقليتك.",
    adminOnly: false,
    async execute(api, message, config) {
        const advices = [
            "ابتدئ يومك بالتخطيط، فـ 10 دقائق من التخطيط توفر عليك ساعات من العشوائية.",
            "الانضباط والالتزام اليومي يصنع نتائج تعجز عنها الموهبة وحدها.",
            "أفضل استثمار في حياتك هو الاستثمار في عقلك وتطوير مهاراتك الذاتية.",
            "لا تخف من الفشل البرمجي أو الحياتي، فكل خطأ يعلمك درساً جديداً.",
            "قلل من تصفح مواقع التواصل دون فائدة، وركز على بناء مشروعك الخاص."
        ];
        const randomAdvice = advices[Math.floor(Math.random() * advices.length)];
        const msg = `💡 [ كبسولة نجاح البوت Luky ] ✨\n\n` +
                    `📝 نصيحة لك: "${randomAdvice}"\n\n` +
                    `👑 يتمنى لك المطور فارس الكوشي يوماً حافلاً بالإنجازات!`;
        api.sendMessage(msg, message.threadID);
    }
};
