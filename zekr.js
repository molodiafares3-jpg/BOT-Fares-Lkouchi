module.exports = {
    name: "ذكر",
    description: "يرسل لك ذكراً أو دعاءً قصيراً لتلطيف الأجواء بالجروب.",
    adminOnly: false,
    async execute(api, message, config) {
        const azkar = [
            "سُبْحَانَ اللَّهِ وَبِحَمْدِهِ ، سُبْحَانَ اللَّهِ الْعَظِيمِ ✨",
            "لا حَوْلَ وَلا قُوَّةَ إِلا بِاللَّهِ الْعَلِيِّ الْعَظِيمِ 🤍",
            "اللَّهُمَّ صَلِّ وَسَلِّمْ وَبَارِكْ عَلَى نَبِيِّنَا مُحَمَّدٍ 🌙",
            "أَسْتَغْفِرُ اللَّهَ الْعَظِيمَ الَّذِي لا إِلَهَ إِلا هُوَ الْحَيُّ الْقَيُّومُ وَأَتُوبُ إِلَيْهِ 🤲",
            "الحَمْدُ للَّهِ حَمْداً كَثِيراً طَيِّباً مُبَارَكاً فِيهِ 🌟"
        ];

        const randomZekr = azkar[Math.floor(Math.random() * azkar.length)];

        const replyMsg = `🕌 [ ذكر ودعاء الجروب ] 🩹\n\n` +
                         `💬 ｢ ${randomZekr} ｣\n\n` +
                         `🤖 الدال على الخير كفاعله، تحيات البوت Luky والمطور فارس الكوشي 👑`;

        api.sendMessage(replyMsg, message.threadID);
    }
};
