module.exports = {
    name: "هيبة",
    description: "يعطيك عبارات واقتباسات فخمة عن الهيبة والثقة بالنفس.",
    adminOnly: false,
    async execute(api, message, config) {
        const royalWords = [
            "الأسد لا يلتفت وراءه ليعرف إذا كانت الكلاب تنبح أم لا، ثق بخطواتك دائماً. 🦁",
            "الهيبة ليست في أن يتحدث الناس عنك، بل في صمتهم التام بمجرد حضورك. 🤫",
            "كن كالملك، ثابتاً في عواصف الحياة، ولا تسمح لأي شيء أن يهز عرش ثقتك بنقسك. 👑",
            "فن الرد يبدأ بالصمت الذكي، فليس كل سؤال يستحق أن تمنحه شرف إجابتك. 🔮",
            "من اعتاد على القمم، لا تبهره الأضواء المزيفة في القاع. ✨"
        ];
        const randomWord = royalWords[Math.floor(Math.random() * royalWords.length)];
        const msg = `👑 [ عبارات الهيبة والفخامة الملكية ] ✨\n\n` +
                    `💬 "${randomWord}"\n\n` +
                    `🤖 بوت التفاعل Luky يحيي الأساطير بلمسة المطور فارس الكوشي 💎`;
        api.sendMessage(msg, message.threadID);
    }
};
