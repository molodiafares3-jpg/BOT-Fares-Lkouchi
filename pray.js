module.exports = {
    name: "دعاء",
    description: "يرسل دعاءً جميلاً ومريحاً للنفس.",
    adminOnly: false,
    async execute(api, message, config) {
        const prayers = [
            "اللَّهُمَّ إِنِّي أَسْأَلُكَ عِلْمًا نَافِعًا، وَرِزْقًا طَيِّبًا، وَعَمَلًا مُتَقَبَّلًا 🤲",
            "رَبَّنَا آتِنَا فِي الدُّنْيَا حَسَنَةً وَفِي الْآخِرَةِ حَسَنَةً وَقِنَا عَذَابَ النَّارِ 🌟",
            "اللَّهُمَّ اشْرَحْ صُدُورَنَا، وَيَسِّرْ أُمُورَنَا، وَاجْعَلْنَا مِنْ سُعَدَاءِ الدُّنْيَا وَالآخِرَةِ 🌙",
            "يَا حَيُّ يَا قَيُّومُ بِرَحْمَتِكَ أَسْتَغِيثُ، أَصْلِحْ لِي شَأْنِي كُلَّهُ وَلَا تَكِلْنِي إِلَى نَفْسِي طَرْفَةَ عَيْنٍ 🩹",
            "اللَّهُمَّ اكْفِنِي بِحَلَالِكَ عَنْ حَرَامِكَ، وَأَغْنِنِي بِفَضْلِكَ عَمَّنْ سِوَاكَ 🤍"
        ];
        const randomPray = prayers[Math.floor(Math.random() * prayers.length)];
        const msg = `🕌 [ نفحات إيمانية ودعاء ] ✨\n\n` +
                    `💬 ｢ ${randomPray} ｣\n\n` +
                    `🤖 البوت العام Luky يجمعنا على الخير، بتوجيه من فارس 👑`;
        api.sendMessage(msg, message.threadID);
    }
};
